#include <iostream>
using namespace std;

int main() {
	int a;
	float b;
	char c;
	double d;
	short s;
	int v[5];
	cout << "sizeof int a: " << sizeof(a) << endl;
	cout << "sizeof float b: " << sizeof(b) << endl;
	cout << "sizeof char c: " << sizeof(c) << endl;
	cout << "sizeof double d: " << sizeof(d) << endl;
	cout << "sizeof short s: " << sizeof(s) << endl;
	cout << "sizeof int v[5]: " << sizeof(v) << endl;
	return 0;
}
